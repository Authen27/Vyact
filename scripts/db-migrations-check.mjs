#!/usr/bin/env node
// FinFlow — DB migrations validator and schema-snapshot regenerator.
//
// `supabase/migrations/<ts>_<name>.sql` is the source of truth for the
// Postgres schema. `db/schema.sql` is a GENERATED human-readable concat of
// every migration in order, kept in the repo so reviewers can read the
// current shape of the schema in one file (and so existing references in
// docs/code keep resolving).
//
// This script:
//   1. Validates migration file naming: `<14+digit-timestamp>_<snake_name>.sql`.
//   2. Refuses duplicate timestamps.
//   3. Refuses empty migrations.
//   4. Regenerates the expected `db/schema.sql` from the ordered migrations
//      and compares against the on-disk copy (line-ending-normalised).
//   5. With `--fix`, writes the regenerated `db/schema.sql` instead of
//      comparing.
//
// Failure modes (exit code 1):
//   - any file in supabase/migrations/ violates the naming rule
//   - duplicate timestamps
//   - an empty migration file
//   - db/schema.sql is missing or drifts from the concat
//
// Usage:
//   node scripts/db-migrations-check.mjs          # validate (CI gate)
//   node scripts/db-migrations-check.mjs --fix    # regenerate db/schema.sql

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const migDir = path.join(repoRoot, 'supabase', 'migrations');
const snapshotPath = path.join(repoRoot, 'db', 'schema.sql');
const fixMode = process.argv.includes('--fix');

const NAME_RE = /^(\d{14,})_([a-z0-9][a-z0-9_]*)\.sql$/;

// ── 1. Discover and validate migration filenames ─────────────────
if (!fs.existsSync(migDir)) {
  console.error(`✗ ${path.relative(repoRoot, migDir)} not found`);
  process.exit(1);
}
const all = fs.readdirSync(migDir).filter(f => f.endsWith('.sql')).sort();
if (all.length === 0) {
  console.error(`✗ ${path.relative(repoRoot, migDir)} is empty — at least one migration required`);
  process.exit(1);
}

const problems = [];
const seenTs = new Map(); // ts → filename
const migrations = [];

for (const file of all) {
  const m = NAME_RE.exec(file);
  if (!m) {
    problems.push(`bad name: ${file} (must match <timestamp>_<snake_name>.sql, 14+ digits)`);
    continue;
  }
  const ts = m[1];
  if (seenTs.has(ts)) {
    problems.push(`duplicate timestamp ${ts}: ${seenTs.get(ts)} and ${file}`);
    continue;
  }
  seenTs.set(ts, file);

  const full = path.join(migDir, file);
  const body = fs.readFileSync(full, 'utf8');
  if (body.trim().length === 0) {
    problems.push(`empty migration: ${file}`);
    continue;
  }
  migrations.push({ file, ts, body });
}

if (problems.length) {
  console.error(`✗ ${problems.length} migration problem(s):`);
  for (const p of problems) console.error(`   ${p}`);
  process.exit(1);
}

// Ensure lexicographic order (timestamps already sort that way).
migrations.sort((a, b) => a.ts.localeCompare(b.ts));

// ── 2. Build the expected schema.sql ─────────────────────────────
const header =
`-- ════════════════════════════════════════════════════════════════
-- FinFlow · Postgres schema (GENERATED — do not edit by hand)
--
-- Source of truth: supabase/migrations/*.sql
-- Regenerated by:  node scripts/db-migrations-check.mjs --fix
-- CI gate:         scripts/automation-run.mjs · "db-migrations"
--
-- To change the schema:
--   1. Add an additive migration at supabase/migrations/<timestamp>_<name>.sql
--   2. Run \`node scripts/db-migrations-check.mjs --fix\` to refresh this file
--   3. Apply the migration to staging (e.g. via the Supabase MCP
--      \`apply_migration\` tool) and the security advisor, per
--      docs/TEST_GOVERNANCE.md §4
-- ════════════════════════════════════════════════════════════════
`;

function section(file) {
  const bar = '-- ─'.padEnd(72, '─');
  return `\n${bar}\n-- supabase/migrations/${file}\n${bar}\n`;
}

const generated = header + migrations.map(m => section(m.file) + (m.body.endsWith('\n') ? m.body : m.body + '\n')).join('');

// Normalise line endings on both sides before comparing so Windows CRLF
// checkouts don't trip the gate.
function lf(s) { return s.replace(/\r\n/g, '\n'); }

const generatedLf = lf(generated);

// ── 3. Compare or write ──────────────────────────────────────────
if (fixMode) {
  fs.writeFileSync(snapshotPath, generatedLf);
  console.log(`✓ Wrote ${path.relative(repoRoot, snapshotPath)} from ${migrations.length} migration(s).`);
  process.exit(0);
}

if (!fs.existsSync(snapshotPath)) {
  console.error(`✗ ${path.relative(repoRoot, snapshotPath)} is missing. Run with --fix to generate.`);
  process.exit(1);
}

const onDiskLf = lf(fs.readFileSync(snapshotPath, 'utf8'));

if (onDiskLf === generatedLf) {
  console.log(`✓ ${path.relative(repoRoot, snapshotPath)} is in sync with ${migrations.length} migration(s) (${onDiskLf.length} chars).`);
  process.exit(0);
}

// Drift — show a short diff hint.
console.error(`✗ ${path.relative(repoRoot, snapshotPath)} has drifted from supabase/migrations/.`);
console.error(`  Expected ${generatedLf.length} chars, found ${onDiskLf.length} chars.`);
const aLines = generatedLf.split('\n');
const bLines = onDiskLf.split('\n');
let firstDiff = -1;
for (let i = 0; i < Math.max(aLines.length, bLines.length); i++) {
  if (aLines[i] !== bLines[i]) { firstDiff = i; break; }
}
if (firstDiff >= 0) {
  console.error(`  First differing line: ${firstDiff + 1}`);
  console.error(`    expected: ${(aLines[firstDiff] ?? '<EOF>').slice(0, 120)}`);
  console.error(`    actual:   ${(bLines[firstDiff] ?? '<EOF>').slice(0, 120)}`);
}
console.error(`  Fix: node scripts/db-migrations-check.mjs --fix`);
process.exit(1);
